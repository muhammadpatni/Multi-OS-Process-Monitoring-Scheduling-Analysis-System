def run_adb(cmd):
    try:
        result = subprocess.run([adb_path] + cmd.split(), capture_output=True, text=True)
        if result.returncode == 0:
            return result.stdout.strip()
    except Exception as e:
        print("ADB error:", e)
    return ""

def map_state(stat):
    mapping = {
        "R": "Running",
        "S": "Sleeping",
        "D": "Uninterruptible Sleep",
        "T": "Stopped",
        "Z": "Zombie",
        "I": "Idle",
        "X": "Dead",
    }
    letter = stat[0] if stat else "?"
    return mapping.get(letter, "Unknown")

def purpose(name):
    if name in process_purpose:
        return process_purpose[name]
    else:
        return "N/A"

def get_all_processes():
    output = run_adb("shell ps -e -o USER,PID,PPID,VSZ,RSS,STAT,NAME")
    procs = []
    lines = output.splitlines()

    for line in lines[1:]:
        parts = line.split()
        if len(parts) < 7:
            continue

        procs.append({
            "user": parts[0],
            "pid": parts[1],
            "ppid": parts[2],
            "vsz": int(parts[3]),
            "rss": int(parts[4]),
            "state": map_state(parts[5]),
            "name": parts[6],
        })

    return procs

def get_cpu_threads():
    output = run_adb("shell top -b -n 1 -o PID")
    cpu_threads = {}

    for line in output.splitlines()[1:]:
        parts = line.split()
        if len(parts) < 10:
            continue

        pid = parts[0]

        try:
            cpu = float(parts[8])
        except:
            cpu = 0.0

        try:
            threads = int(parts[4])
        except:
            threads = 0

        cpu_threads[pid] = {
            "cpu": cpu,
            "threads": threads
        }

    return cpu_threads

def get_sched(pid):
    output = run_adb(f"shell cat /proc/{pid}/sched")
    policy = None

    for line in output.splitlines():
        if "policy" in line:
            try:
                policy = int(line.split(":")[1].strip())
            except:
                policy = None

    return policy


def map_scheduler(policy):
    sched_map = {
        0: "CFS (Completely Fair Scheduler) default",
        1: "SCHED_FIFO (Real-time, FIFO)",
        2: "SCHED_RR (Real-time, Round-Robin)",
        3: "SCHED_DEADLINE (Deadline-based, Real-time)"
    }
    return sched_map.get(policy, "Unknown / Adaptive")

def inspect_processes():
    global Countprocesses

    procs = get_all_processes()
    cpu_threads = get_cpu_threads()

    print("=" * 120)
    print("ANDROID PROCESS INSPECTOR  FULL STATE + PURPOSE")
    print("=" * 120)

    all_data = []

    for p in procs:
        pid = p["pid"]
        name = p["name"]

        ct = cpu_threads.get(pid, {})
        cpu = ct.get("cpu", 0.0)
        threads = ct.get("threads", 0)

        policy = get_sched(pid)
        scheduler = map_scheduler(policy)

        info = {
            "pid": pid,
            "name": name,
            "user": p["user"],
            "ppid": p["ppid"],
            "threads": threads,
            "cpu_percent": cpu,
            "rss_kb": p["rss"],
            "vms_kb": p["vsz"],
            "Purpose":purpose(name),
            "state": p["state"],
            "scheduler": scheduler,
        }

        Countprocesses += 1
        all_data.append(info)
    return all_data


